'use client';

/**
 * BreadcrumbJsonLd - Structured data for breadcrumb navigation
 * Helps search engines understand site hierarchy for rich snippets
 */
export default function BreadcrumbJsonLd({ items, baseUrl = 'https://www.sifrausa.com' }) {
  if (!items || items.length === 0) return null;

  const jsonLd = {
    '@context': 'https://schema.org',
    '@type': 'BreadcrumbList',
    itemListElement: items.map((item, index) => ({
      '@type': 'ListItem',
      position: index + 1,
      name: item.name,
      item: item.url?.startsWith('http') ? item.url : `${baseUrl}${item.url}`,
    })),
  };

  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: JSON.stringify(jsonLd) }}
    />
  );
}
